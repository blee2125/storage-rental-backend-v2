const mongoose = require('mongoose');

const dataSchema = new mongoose.Schema({
    name: {
        required: true,
        type: String
    },
    email: {
        required: true,
        type: String
    },
    phone: {
        required: true,
        type: String
    }
})

dataSchema.set('timestamps', true);

module.exports = mongoose.model('Customer', dataSchema)